    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed

        Despesa despesas = new Despesa();
        DaoDespesa daoDespesas = new DaoDespesa();

        despesas.setCodUnidade(Integer.parseInt(jTextFieldCodUnidade.getText()));
        despesas.setDescricao(jTextFieldDescricao.getText()); 
        despesas.setTipo(jTextFieldTipo.getText()); 
        despesas.setValor(Double.parseDouble(jTextFieldValor.getText()));   
        try{
            despesas.setVencimento(Date.valueOf(jTextFieldVencimento.getText()));
        }catch(Exception e){            
            JOptionPane.showMessageDialog(null,"Dada Formado Invalido: "
                    + "Digitar no formado yyyy/MM/dd",
                    "Erro",JOptionPane.ERROR_MESSAGE); 
            return;
        }
        despesas.setStatusPgto(Boolean.valueOf(jTextFieldStatus.getText()));

        daoDespesas.cadastrarDespesa(despesas);
        CarregarTabela();
        LimparjTextField();
    }//GEN-LAST:event_jButtonSalvarActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed

        Despesa despesas =new Despesa();
        DaoDespesa daoDespesa =new DaoDespesa();
        Integer index = jTableDespesa.getSelectedRow();

        despesas =daoDespesa.listarDespesa().get(index);
        despesas.setCodUnidade(Integer.parseInt(jTextFieldCodUnidade.getText()));
        despesas.setDescricao(jTextFieldDescricao.getText()); 
        despesas.setTipo(jTextFieldTipo.getText()); 
        despesas.setValor(Double.parseDouble(jTextFieldValor.getText()));        
        despesas.setVencimento(Date.valueOf(jTextFieldVencimento.getText()));
        despesas.setStatusPgto(Boolean.valueOf(jTextFieldStatus.getText()));;

        daoDespesa.editarDespesa(despesas);
        CarregarTabela();
        LimparjTextField();
        DesabilitarJTextField();
        DesabilitarJbuttom();
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed

        Despesa despesas =new Despesa();
        DaoDespesa daoDespesas = new DaoDespesa();
        Integer index = jTableDespesa.getSelectedRow();
        despesas = daoDespesas.listarDespesa().get(index);
        daoDespesas.removerDespesa(Integer.parseInt(jTextFieldCodDespesa.getText()));
        CarregarTabela();
        LimparjTextField();
        DesabilitarJTextField();
        DesabilitarJbuttom();
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jTableDespesaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableDespesaMouseClicked

        Despesa despesas = new Despesa();
        DaoDespesa daoDespesas =new DaoDespesa();
        Integer index = jTableDespesa.getSelectedRow();
        despesas= daoDespesas.listarDespesa().get(index);

        jTextFieldCodDespesa.setText(String.valueOf(despesas.getCodDespesa()));
        jTextFieldCodUnidade.setText(String.valueOf(despesas.getCodUnidade()));
        jTextFieldDescricao.setText(despesas.getDescricao());      
        jTextFieldTipo.setText(String.valueOf(despesas.getTipo())); 
        jTextFieldValor.setText(String.valueOf(despesas.getValor()));
        jTextFieldVencimento.setText(String.valueOf(despesas.getVencimento()));
        jTextFieldStatus.setText(String.valueOf(despesas.getStatusPgto()));

        jButtonSalvar.setEnabled(false);
        jButtonEditar.setEnabled(true);
        jButtonExcluir.setEnabled(true);
        HabilitarJTextField();
    }//GEN-LAST:event_jTableDespesaMouseClicked

